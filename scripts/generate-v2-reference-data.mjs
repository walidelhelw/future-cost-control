import fs from "node:fs";
import path from "node:path";
import vm from "node:vm";
import ts from "typescript";

const root = process.cwd();

const sources = [
  {
    file: "src/data/productivity-templates.ts",
    exportName: "productivityTemplates",
    kind: "productivity_template",
    code: (item) => item.code,
    nameAr: (item) => item.nameAr,
    nameEn: (item) => item.nameEn ?? null,
  },
  {
    file: "src/data/boq-items.ts",
    exportName: "boqItems",
    kind: "boq_item",
    code: (item) => item.id,
    nameAr: (item) => item.nameAr,
    nameEn: (item) => item.nameEn,
  },
  {
    file: "src/data/crew-roles.ts",
    exportName: "crewRoles",
    kind: "crew_role",
    code: (item) => item.code,
    nameAr: (item) => item.nameAr,
    nameEn: (item) => item.nameEn,
  },
  {
    file: "src/data/condition-factors.ts",
    exportName: "conditionFactors",
    kind: "condition_factor",
    code: (item) => item.code,
    nameAr: (item) => item.nameAr,
    nameEn: (item) => item.nameEn,
  },
  {
    file: "src/data/risk-catalog.ts",
    exportName: "riskCatalog",
    kind: "risk_catalog",
    code: (item) => item.id,
    nameAr: (item) => item.statementAr,
    nameEn: (item) => item.statementEn,
  },
];

function loadExport(file, exportName) {
  const source = fs.readFileSync(path.join(root, file), "utf8");
  const output = ts.transpileModule(source, {
    compilerOptions: {
      esModuleInterop: true,
      module: ts.ModuleKind.CommonJS,
      target: ts.ScriptTarget.ES2020,
    },
  }).outputText;
  const context = {
    exports: {},
    module: { exports: {} },
    require: () => ({}),
  };
  vm.runInNewContext(output, context, { filename: file });
  return context.module.exports[exportName] ?? context.exports[exportName];
}

function sql(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replaceAll("'", "''")}'`;
}

const rows = [];
for (const source of sources) {
  const items = loadExport(source.file, source.exportName);
  for (const item of items) {
    rows.push({
      kind: source.kind,
      code: source.code(item),
      nameAr: source.nameAr(item),
      nameEn: source.nameEn(item),
      payload: item,
      source: source.file,
    });
  }
}

const values = rows
  .map((row) =>
    [
      sql(row.kind),
      sql(row.code),
      sql(row.nameAr),
      sql(row.nameEn),
      `${sql(JSON.stringify(row.payload))}::jsonb`,
      "true",
      "CURRENT_DATE",
      sql(row.source),
    ].join(", ")
  )
  .map((value) => `  (${value})`)
  .join(",\n");

const output = `-- Generated by scripts/generate-v2-reference-data.mjs.\nINSERT INTO v2_reference_data\n  (kind, code, name_ar, name_en, payload, is_global, effective_from, source)\nVALUES\n${values}\nON CONFLICT DO NOTHING;\n`;

fs.mkdirSync(path.join(root, "supabase", "seed"), { recursive: true });
fs.writeFileSync(path.join(root, "supabase", "seed", "v2_reference_data.sql"), output);
console.log(`Wrote ${rows.length} V2 reference rows.`);
